package _constructor;

public class DefaultConstructor {

	int i;
	String s;
	float f;
	char c;
	
	void display() {
		System.out.println("default value to integer :" + i);
		System.out.println("default value to string :" + s);
		System.out.println("default value to float :" + f);
		System.out.println("default value to char :" + c);
		System.out.println(" ");
	}
}
